export function translate(locale: Locale, key: TKey, vars?: Record<string, string | number>): string {
  const raw = dicts[locale]?.[key] ?? dicts.en[key] ?? key;
  if (!vars) return raw;
  return Object.entries(vars).reduce((s, [k, v]) => s.replace(`{${k}}`, String(v)), raw);
}

export function detectInitialLocale(): Locale {
  try {
    const stored = localStorage.getItem('app-locale');
    if (stored && LOCALES.some(l => l.code === stored)) return stored as Locale;
  } catch {}
  const nav = (typeof navigator !== 'undefined' ? navigator.language || 'en' : 'en').toLowerCase();
  if (nav.startsWith('zh')) return 'zh-CN';
  if (nav.startsWith('tr')) return 'tr';
  if (nav.startsWith('ja')) return 'ja';
  if (nav.startsWith('ko')) return 'ko';
  if (nav.startsWith('de')) return 'de';
  if (nav.startsWith('fr')) return 'fr';
  if (nav.startsWith('es')) return 'es';
  if (nav.startsWith('pt')) return 'pt-BR';
  if (nav.startsWith('ru')) return 'ru';
  return 'en';
}
